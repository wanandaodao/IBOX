<script>
export default {
  data() {
    return {
      currentAssetTab: 0,
      currentSubTab: 0,
      assetTabs: ['藏品', '盲盒', '元宇宙'],
    }
  },
  methods: {
    copyWalletAddress() {
      const walletAddress = '0x30ad...18bb1c0'
      uni.setClipboardData({
        data: walletAddress,
        success: () => {
          uni.showToast({
            title: '复制成功',
            icon: 'success'
          })
        }
      })
    },
    navigateTo(url) {
      uni.navigateTo({
        url,
        success: () => {
          console.log('页面跳转成功：', url);
        },
        fail: (err) => {
          console.error('页面跳转失败：', err);
          uni.showToast({
            title: '页面跳转失败',
            icon: 'none'
          });
        }
      });
    },
    handleSwiperChange(e) {
      this.currentAssetTab = e.detail.current
    },
    switchAssetTab(index) {
      this.currentAssetTab = index
    },
    switchSubTab(index) {
      this.currentSubTab = index
    },
    toHome(index) {
      // 将index值存储到本地存储中，并添加标记表示这是来自跳转
      uni.setStorageSync('tabbarIndex', index)
      uni.setStorageSync('isFromNavigation', true)
      // 跳转到home页面，禁用动画
      uni.navigateTo({
        url: '/pages/home/home',
        animationType: 'none',
        animationDuration: 0
      })
    }
  }
}
</script>

